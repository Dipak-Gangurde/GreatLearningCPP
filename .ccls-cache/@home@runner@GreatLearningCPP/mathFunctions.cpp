// Write a cpp program that shows use of math library functions
#include <iostream>
#include <math.h>
using namespace std;

int main(){
  int a = 4;
  

  cout<<"Sin: "<<sin(a);
  cout<<"\nCos: "<<cos(a);
  cout<<"\nTan: "<<tan(a);
  cout<<"\nSquare root: "<<sqrt(a);
  cout<<"\nPower: "<<pow(a, 2);

  return 0;
}